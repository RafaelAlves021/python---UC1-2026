opção = int(input('Dígite uma opção'))

match opção:
 case 1:
    print('Voce escolheu a opção 1: Ver saldo.')
 case 2:
    print('Voce escolheu a opção 2: Fazer saque.')
 case 3:
    print('Voce escolheu a opção 3: Falar com atendimento') 
 case _:
    print('Opção inválida! Escolha um número de 1 a 3.') 
       
    

